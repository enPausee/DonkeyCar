<h3 id="paiement">Comment puis je payer ma location</h3>
<br>
<div id="paiement">
    <p>La location sera payée au retour du véhicule, sauf si vous avez effectué une réservation au tarif prépayé (offre
        Payez en ligne). Dans ce cas, votre carte de crédit sera débitée du montant de la location lors de la
        réservation.
        Pour des réservations non prépayées, un dépôt vous sera exigé au moment du retrait et vous devrez effectuer le
        paiement à la fin de la location.
        Nous acceptons les moyens de paiement suivants : cartes de crédit, paypal. Cela
        peut varier selon le type de véhicule que vous louez.</p>
</div>
