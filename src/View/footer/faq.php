<h3 id="consultation">Puis je consultez les tarifs sans effectuez de reservation</h3>
<br>
<div id="consultation">
    <p>Oui. Vous pouvez demander un devis DonkeyCar et vérifier la disponibilité du véhicule sans effectuer de
        réservation. Remplissez uniquement la page d’accueil sur votre site DonkeyCar et cliquez sur « Obtenir un devis
        ». Pour sauvegarder ou recevoir un devis, cliquez sur « Détails du véhicule et du tarif ». Vous y trouverez plus
        de détails concernant le véhicule et le tarif. Si vous souhaitez effectuer une réservation à ce moment-là et
        avec ces détails, il vous suffit de continuer en cliquant sur le bouton « Réserver »</p>
</div>
<hr>
<h3 id="annulation">Est il possible de modifier ou annuler ma réservation après avoir recu ma confirmation</h3>
<br>
<div>
    <p>Oui. DonkeyCar vous permet de modifier ou d’annuler votre réservation en ligne après confirmation. L'annulation
        est gratuite jusqu'à 48h avant le départ. Vous devez simplement cliquer sur le lien « Voir/modifier/annuler une
        réservation » dans la rubrique « Mon compte ».
        Si vous n’annulez pas la réservation, que vous ne retirez pas le véhicule avant l’heure de fermeture de
        l’agence, ou que vous annulez au delà du préavis de 48h, des frais de 50 € ou d’un montant équivalent dans la
        devise du pays de location vous seront facturés. Veuillez noter que vous pouvez uniquement modifier ou annuler
        une réservation que vous avez effectuée vous-même en ligne. Vous recevrez une confirmation de la modification ou
        de l’annulation par e-mail.</p>
</div>
<hr>
<h3 id="limite">Existe t'il une limite de kilométrage pour ma location</h3>
<br>
<div>
    <p>
        DonkeyCar propose deux types de tarifs :
        - Le tarif incluant le kilométrage illimité : vous pouvez conduire autant que vous le souhaitez. Toutefois, nous
        recommandons qu’aucun conducteur ne soit au volant pendant une durée prolongée et/ou sans se reposer
        correctement ;
        - Le tarif incluant un kilométrage limité : nous vous facturerons un supplément pour tout kilométrage dépassant
        cette limite. Les pages de notre service de réservation en ligne relatives aux tarifs indiquent si le
        kilométrage illimité est inclus. Vous pouvez également poser la question lors du retrait de votre voiture.
    </p>
</div>
