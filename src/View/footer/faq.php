<h3 id="depot">Un dépôt de garantie sera t'il prélevé lorsque je retire la voiture</h3>
<br>
<div>
    Nous devons estimer les frais de location de la voiture et obtenir un numéro d’autorisation de votre banque.
    Cette estimation repose sur :
    <ul>
        <li>la durée estimée de location ;</li>
        <li>le tarif en vigueur, taxes comprises ;</li>
        <li>les frais imprévus, notamment le carburant.</li>
    </ul>
    Ce montant n’est pas débité. Il demeure sur le compte du titulaire de la carte jusqu’au moment où le montant
    final de la location est débité. Dans certains pays, des exceptions existent toutefois. Pour plus de précisions,
    veuillez vous reporter aux informations générales relatives à la location. Les dépôts de garantie et le paiement
    en espèces des locations sont limités au sein du réseau DonkeyCar (là où ils sont acceptés, les dépôts en
    espèces
    doivent être accompagnés de formulaires supplémentaires prouvant l’identité – veuillez vérifier ces informations
    auprès de votre Centre de réservation DonkeyCar)
    </p>
</div>
